public class Machine {
    private int power;
    private int height;
    private String color;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getPower(){
        return power;
    }
    public void setPower(int power){
        this.power = power;
    }

    public int getHeight(){
        return height;
    }
    public void setHeight(int height){
        this.height = height;
    }
    


}
